-- تنظيف بيانات العرض التجريبي المتراكمة في متجركم الحقيقي (متجر البيان
-- سابقًا، الآن "متجر غذائك" بعد إعادة التسمية).
--
-- السبب: كل نشر سابق على Render كان يُعيد تشغيل seed.ts كاملاً، وثلاث
-- جداول فيه (messages، knowledge_chunks، ticket_events) كانت تُدرج نسخة
-- جديدة في كل مرة دون أي فحص تكرار حقيقي. تم إصلاح هذا في الكود (الملف
-- المُرسَل سابقًا)، لكن النسخ المتراكمة من النشرات السابقة ما تزال موجودة
-- في قاعدة بياناتكم الحية ولن تختفي من تلقاء نفسها.
--
-- طريقة الاستخدام:
--   1) افتح Neon (أو أي أداة SQL تديرون بها القاعدة الحقيقية المرتبطة بـ
--      Render — الرابط عادة في متغير DATABASE_URL على Render).
--   2) شغّل قسم "١) تشخيص" أولاً فقط، واقرأ النتائج.
--   3) لا تشغّل قسم "٢) التنظيف" إلا بعد التأكد أن المحتوى الظاهر في
--      التشخيص هو فعلاً المحتوى التجريبي الوهمي الموصوف أدناه، وليس أي
--      محتوى حقيقي أضفتموه بأنفسكم لاحقًا بالمصادفة على نفس المعرّفات.
--
-- كل شيء هنا يستهدف فقط محتوى العرض التجريبي المعروف بمعرّفات ثابتة
-- (محادثة "سارة العتيبي" الوهمية، تذكرة "تأخر شحنة" الوهمية) — لا يمس أي
-- محادثة أو تذكرة أو عميل حقيقي وصل عبر واتساب الفعلي أو رابط المحاكاة.

-- ============================================================
-- ١) تشخيص — قراءة فقط، آمن 100%، لا يغيّر أي شيء
-- ============================================================

-- كم رسالة متراكمة في المحادثة التجريبية (يجب أن يكون العدد كبيرًا،
-- مضاعفًا لعدد مرات النشر — مثلاً لو نشرتم 8 مرات، تتوقعون قرابة 32 رسالة
-- بدل 4 الأصلية):
SELECT count(*) AS demo_messages_count
FROM messages
WHERE conversation_id = '00000000-0000-0000-0000-0000000000c1';

-- كم مقطع معرفة متراكم (نفس المنطق، مضاعف عدد النشرات × 2):
SELECT count(*) AS demo_knowledge_chunks_count
FROM knowledge_chunks
WHERE source_id = '00000000-0000-0000-0000-0000000000a1';

-- محتوى هذه المقاطع بالضبط — تأكد أنها فقط سطرا "سياسة الاسترجاع" و"الشحن
-- داخل الرياض" المكرران، وليس فيها أي محتوى حقيقي أضفتموه لاحقًا عن
-- منتجاتكم الفعلية:
SELECT id, content, created_at
FROM knowledge_chunks
WHERE source_id = '00000000-0000-0000-0000-0000000000a1'
ORDER BY created_at;

-- كم حدث تذكرة متراكم:
SELECT count(*) AS demo_ticket_events_count
FROM ticket_events
WHERE ticket_id = '00000000-0000-0000-0000-0000000000a3';


-- ============================================================
-- ٢) التنظيف — يحذف فعليًا. شغّله فقط بعد مراجعة نتائج القسم أعلاه.
--    الترتيب مهم (يحترم الروابط بين الجداول) — لا تُغيّر الترتيب.
-- ============================================================

-- سجلات الذكاء الاصطناعي واقتراحات المعرفة المرتبطة بالمحادثة الوهمية
DELETE FROM ai_response_logs WHERE conversation_id = '00000000-0000-0000-0000-0000000000c1';
DELETE FROM ai_suggested_knowledge WHERE conversation_id = '00000000-0000-0000-0000-0000000000c1';

-- كل رسائل محادثة "سارة العتيبي" الوهمية (الفستان الأزرق، سياسة الاسترجاع)
DELETE FROM messages WHERE conversation_id = '00000000-0000-0000-0000-0000000000c1';

-- أحداث التذكرة الوهمية، ثم التذكرة نفسها
DELETE FROM ticket_events WHERE ticket_id = '00000000-0000-0000-0000-0000000000a3';
DELETE FROM tickets WHERE id = '00000000-0000-0000-0000-0000000000a3';

-- المحادثة والعميل الوهميان أنفسهما (بعد حذف كل ما يشير إليهما أعلاه)
DELETE FROM conversations WHERE id = '00000000-0000-0000-0000-0000000000c1';
DELETE FROM customers WHERE external_id = 'demo-customer-sara';

-- مقاطع المعرفة التجريبية المكررة — فقط السطرين المعروفين بالضبط، حتى لو
-- أضفتم محتوى حقيقيًا آخر داخل نفس المصدر لاحقًا فلن يُلمس:
DELETE FROM knowledge_chunks
WHERE source_id = '00000000-0000-0000-0000-0000000000a1'
  AND content IN (
    'سياسة الاسترجاع: يمكن استرجاع أي قطعة خلال 14 يومًا من الاستلام بحالتها الأصلية وبطاقتها مرفقة.',
    'الشحن داخل الرياض يستغرق من يومين إلى أربعة أيام عمل.'
  );

